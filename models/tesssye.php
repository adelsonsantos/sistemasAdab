CREATE TABLE portal.Gerencia(
Ger_id serial PRIMARY KEY,
Ger_nome varchar(60) not null
);
CREATE TABLE portal.Coordenadoria_gerencia(
Cog_id serial PRIMARY KEY,
Id_coordenadoria int NOT NULL,
Ger_id int NOT NULL,
FOREIGN KEY (Id_coordenadoria) REFERENCES diaria.Coordenadoria(Id_coordenadoria),
FOREIGN KEY (Ger_id) REFERENCES portal.Gerencia(Ger_id)
);
CREATE TABLE portal.Contato(
Con_id serial PRIMARY KEY,
Con_nome varchar(60) NOT NULL,
Con_telefone varchar(12) NOT NULL
);
CREATE TABLE portal.Contato_coordenadoria (
Coc_id serial PRIMARY KEY,
Con_id int NOT NULL,
Id_coordenadoria int NOT NULL,
FOREIGN KEY (Con_id) REFERENCES portal.Contato(Con_id),
FOREIGN KEY (Id_coordenadoria) REFERENCES diaria.Coordenadoria(Id_coordenadoria)
);
CREATE TABLE portal.Contato_gerencia(
Cge_id serial PRIMARY KEY,
Con_id int NOT NULL,
Ger_id int NOT NULL,
FOREIGN KEY (Con_id) REFERENCES portal.Contato(Con_id),
FOREIGN KEY (Ger_id) REFERENCES portal.Gerencia(Ger_id)
);
CREATE TABLE portal.Computador (
Com_id serial PRIMARY KEY,
Com_mac char(17) NOT NULL,
Id_coordenadoria int NOT NULL,
Ger_id int NOT NULL,
FOREIGN KEY (Id_coordenadoria) REFERENCES diaria.Coordenadoria(Id_coordenadoria),
FOREIGN KEY (Ger_id) REFERENCES portal.Gerencia(Ger_id)
);
CREATE TABLE portal.Log_computador(
Loc_id serial PRIMARY KEY,
Com_id int NOT NULL,
FOREIGN KEY (Com_id) REFERENCES portal.Computador(Com_id),
Loc_data_hora timestamptz NOT NULL,
Loc_mac char(17) NOT NULL,
Loc_memoria int NOT NULL,
Loc_processador varchar(50) NOT NULL,
Loc_hd int NOT NULL,
Loc_ip_publico varchar(15) NOT NULL);

